public class MAT {
    int angkaA;
    int angkaB;
    
    
    int pertambahan(){
        return angkaA + angkaB;
    }
    int perkalian(){
        return angkaA * angkaB;
    }
    int modulus(){
        return angkaA % angkaB;
    }
}
